/**
 * adminI18n — the unified ops console speaks English and Korean ONLY.
 * (German was removed 2026-07-12 by owner decision; the console is internal.)
 * Independent from the app Language type — the admin language is its own
 * state, persisted in localStorage, toggled with text buttons (no flags).
 */

export type AdminLang = 'en' | 'ko';

const LS_KEY = 'hpdb-admin-lang';

export const loadAdminLang = (): AdminLang =>
  (typeof localStorage !== 'undefined' && localStorage.getItem(LS_KEY) === 'ko') ? 'ko' : 'en';

export const saveAdminLang = (lang: AdminLang): void => {
  try { localStorage.setItem(LS_KEY, lang); } catch { /* ignore */ }
};

